const diaSemana = 8;

switch(diaSemana){
    case 0 : console.log('Domingo');
    break;

    case 1 : console.log('Segunda-Feira');
    break;

    case 2 : console.log('Quarta-Feira');
    break;

    case 3 : console.log("Quinta-Feira");
    break;

    case 4 : console.log("sexta-feira");
    break;

    case 5 : console.log("Sábado");
    break;

    default:console.log('Dia não existente') //ELSE
}